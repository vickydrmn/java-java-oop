package modul4.ke3A;

public class Lingkaran implements BangunDatar{
    public int jarijari;
    public double Luas;
    public double Keliling;

    public double luas() {
        return Luas = 3.14 * jarijari * jarijari;
    }
    public double keliling(){
        return Keliling = 2 * 3.14 * jarijari;
    }
}
